/**
 * Shortcode for icons
 * Usage: [marfinex_icon name="shield-check" size="32" class="my-class"]
 */
function marfinex_icon_shortcode( $atts ) {
	$atts = shortcode_atts( array(
		'name'  => 'check-circle',
		'size'  => 24,
		'class' => 'marfinex-icon',
	), $atts );

	return marfinex_get_icon( $atts['name'], intval( $atts['size'] ), $atts['class'] );
}
add_shortcode( 'marfinex_icon', 'marfinex_icon_shortcode' );

/**
 * Shortcode for LatePoint booking button
 * Usage: [marfinex_booking text="Agendar Consulta"]
 */
